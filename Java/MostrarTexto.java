public class MostrarTexto {
    public static void main(String[] args) {
        System.out.println("hola mundo");
    }
}
/*  
* observaciones.
    el metodo principal debe ser llamado main si o si, es parte de la sintaxis de java.
    el argumento string[] args, recupera datos de la terminal y viceversa en este caso.
    el metodo principal es el que se ejecuta por ello no permite que no lleve el nombre "main".
    static aclara que se instancia inmediatamente sin objetos, por ello debe estar en main si no depende de nada.
    public indica que es accesible por todas las clases o subclases y en consecuencia metodos.
*/